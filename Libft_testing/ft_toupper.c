char	*ft_toupper(char *str)
{
	int itt;

	itt = 0;
	while (str[itt])
	{
		if (str[itt] >= 'a' && str[itt] <= 'z')
			str[itt] += ('A' - 'a');
		itt++;
	}
	return (str);
}
