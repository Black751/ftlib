#include "ftlib.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	itt;

	itt = 1;
	while (itt < n && *s1 == *s2 && *s1)
	{
		s1++;
		s2++;
		itt++;
	}
	return (*s1 - *s2);
}
