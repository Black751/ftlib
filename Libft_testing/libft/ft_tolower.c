char	*ft_tolower(char *str)
{
	int itt;

	itt = 0;
	while (str[itt])
	{
		if (str[itt] >= 'A' && str[itt] <= 'Z')
			str[itt] += ('a' - 'A');
		itt++;
	}
	return (str);
}
